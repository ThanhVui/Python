hrs = input('Enter Hours: ')
hrs = float(hrs)

hourly_rate = input('Enter Hourly Rate: ')
hourly_rate = float(hourly_rate)

gross_pay = hourly_rate * hrs
print("Gross pay:", gross_pay)
# This code calculates the gross pay based on hours worked and hourly rate.
# It prompts the user for input, converts the input to a float, and then calculates the gross pay.